int compress(vector<char>& chars) {
  int size = chars.size();
  vector<char> ans;
  int freq = 1;
  string str = "";
  if (size > 1) {
    for (int i = 0; i < size; i++) {
      if (i + 1 == size || chars[i] != chars[i + 1]) {
        ans.push_back(chars[i]);
        if (freq > 1) {
          str = to_string(freq);
          int s = str.size();
          for (int i = 0; i < s; i++) {
            ans.push_back(str[i]);
          }
        }
        freq = 1;
      } else {
        freq++;
      }
    }
    chars = ans;
  }
  size = chars.size();
  for (int i = 0; i < size; i++) {
    cout << chars[i] << " ";
  }
  return size;
}

