// time O(n), space O(1)
bool triplet(vector<int> nums) {
  int size = nums.size();
  int left = INT8_MAX;
  int mid = INT16_MAX;

  for (int i = 0; i < size; i++) {
    if (nums[i] > mid) {
      return true;
    } else if (nums[i] < mid && nums[i] > left) {
      mid = nums[i];
    } else if (nums[i] < left) {
      left = nums[i];
    }
  }
  return false;
}

